create table contacts (
  id bigint generated by default as identity primary key,
  nom text,
  email text not null,
  message text not null,
  cree_le timestamptz not null default now()
);

alter table contacts enable row level security;

-- Tout le monde peut envoyer un message
create policy "envoi public" on contacts
  for insert with check (true);

-- Pas de policy select : personne (même connecté) ne peut lire les messages
-- via l'API publique. Toi seul les vois, via Supabase → Table Editor →
-- contacts (le Dashboard passe par la clé service_role qui contourne RLS).


-- =============================================================================
-- Historique des passages (commentaires + balises laissés sur un refuge)
-- Partagé entre tous les visiteurs, comme la table refuges.
-- =============================================================================

create table passages (
  id bigint generated by default as identity primary key,
  refuge_id text not null references refuges(id) on delete cascade,
  user_id uuid references auth.users(id) on delete cascade,
  date text not null,
  commentaire text,
  balises text[],
  cree_le timestamptz not null default now()
);

alter table passages enable row level security;

-- Tout le monde peut lire l'historique (utile pour voir l'état récent d'un refuge)
create policy "lecture publique passages" on passages
  for select using (true);

-- Seuls les comptes connectés peuvent ajouter un passage, et seulement en
-- leur propre nom (impossible de poster au nom de quelqu'un d'autre)
create policy "ajout par compte connecte" on passages
  for insert with check (auth.uid() = user_id);

-- Chacun peut supprimer SES PROPRES passages, jamais ceux des autres
create policy "suppression par l'auteur" on passages
  for delete using (auth.uid() = user_id);

