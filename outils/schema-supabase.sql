-- =============================================================================
-- Formulaire de contact (mentions légales) — écriture publique, lecture
-- réservée à l'admin (toi, via le Dashboard Supabase, jamais exposée sur le
-- site). Évite de publier ton email en clair sur la page mentions légales.
